    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel Model = (DefaultTableModel)tickettable.getModel();
        String tbl_id = Model.getValueAt(tickettable.getSelectedRow(), 0).toString();
        String exp_date  = Model.getValueAt(tickettable.getSelectedRow(), 0).toString();
        String price  = Model.getValueAt(tickettable.getSelectedRow(), 0).toString();
        String seat  = Model.getValueAt(tickettable.getSelectedRow(), 0).toString();

        int row = tickettable.getSelectedRow();

        String cell = tickettable.getModel().getValueAt(row,0).toString();
        try {

            PreparedStatement pst = con.prepareStatement("update ticket_booking set Ticket_id = ?, Expiry_date = ?, Ticket_price = ?, Seat_number = ? where id ="+cell);

            pst.setString(1, tbl_id);
            pst.setString(2, exp_date);
            pst.setString(3, price);
            pst.setString(4, seat);
            pst.executeUpdate();
        } catch (Exception e) {
        }
    }//GEN-LAST:event_updatebuttonActionPerformed

