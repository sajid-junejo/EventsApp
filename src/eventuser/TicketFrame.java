    private void showtableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showtableActionPerformed
        // TODO add your handling code here:
         try {
            PreparedStatement preparedStatement = con.prepareStatement("Select Ticket_id, Expiry_date, Ticket_price, Seat_number from ticket_booking");
            ResultSet resultSet = preparedStatement.executeQuery();
             ResultSetMetaData rsmd = resultSet.getMetaData();
             DefaultTableModel Model = (DefaultTableModel)tickettable.getModel();
             int cols = rsmd.getColumnCount();
             String[] colname = new String[cols];
             for(int i=0;i<cols;i++){
             colname[i] = rsmd.getColumnName(i+1);
             Model.setColumnIdentifiers(colname);
             while(resultSet.next()){
                  String id = resultSet.getString(1);
                  String date = resultSet.getString(2);
                  String tktprice = resultSet.getString(3);
                  String seatnumber = resultSet.getString(4);
                 String[] row ={id,date,tktprice,seatnumber};
                 Model.addRow(row);
                 System.out.println(row);
             }
             }
             
             
            } catch (Exception e) {
        }
    }//GEN-LAST:event_showtableActionPerformed

